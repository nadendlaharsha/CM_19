"""
ROUGE Evaluation Metrics for Paper Publication
----------------------------------------------
Prerequisite: 
pip install rouge-score pandas

This script compares your project's generated summaries against reference (human-written or ground-truth) summaries. It also computes scores for previous baseline papers to showcase your accuracy improvements in your research paper.
"""

from rouge_score import rouge_scorer
import pandas as pd

def calculate_rouge(reference, candidate):
    """Calculates ROUGE-1, ROUGE-2, and ROUGE-L F1 scores."""
    scorer = rouge_scorer.RougeScorer(['rouge1', 'rouge2', 'rougeL'], use_stemmer=True)
    scores = scorer.score(reference, candidate)
    
    # Returning F1-Score scaled to 0-100% (Standard for Research Papers)
    return {
        'ROUGE-1 Precision': round(scores['rouge1'].precision * 100, 2),
        'ROUGE-1 Recall': round(scores['rouge1'].recall * 100, 2),
        'ROUGE-1 F1': round(scores['rouge1'].fmeasure * 100, 2),
        
        'ROUGE-2 F1': round(scores['rouge2'].fmeasure * 100, 2),
        'ROUGE-L F1': round(scores['rougeL'].fmeasure * 100, 2)
    }

def main():
    # 1. Provide the Ground Truth (Reference) Summary
    # In a real evaluation, this is the human-written summary or the transcript's actual summary.
    reference_summary = (
        "This video explains the fundamentals of artificial intelligence and machine learning. "
        "It covers neural networks, deep learning applications, and the future of AI in industries. "
        "The speaker emphasizes the importance of data quality for training accurate models."
    )

    # 2. Provide Summaries from Previous Papers (Baselines)
    # Replace this with the output from older models you are comparing against.
    previous_paper_summary = (
        "The video is about artificial intelligence and ML. Neural networks and deep learning "
        "are discussed. Data quality is important."
    )

    # 3. Provide Summary from YOUR Project (TextRank + PCA / Gemini)
    # Replace this with the output generated by your application.
    my_project_summary = (
        "This video discusses artificial intelligence and machine learning fundamentals. "
        "It highlights neural networks, deep learning applications in various industries, and future trends. "
        "Data quality is emphasized as crucial for training accurate AI models."
    )

    print("Evaluating ROUGE Metrics for Paper Publication...\n")

    # Calculate scores
    baseline_scores = calculate_rouge(reference_summary, previous_paper_summary)
    my_project_scores = calculate_rouge(reference_summary, my_project_summary)

    # Isolate relevant F1-scores for the final table formatting
    compare_data = [
        {
            "Model / Approach": "Previous Paper (Baseline)", 
            "ROUGE-1 (F1)": baseline_scores['ROUGE-1 F1'],
            "ROUGE-2 (F1)": baseline_scores['ROUGE-2 F1'],
            "ROUGE-L (F1)": baseline_scores['ROUGE-L F1']
        },
        {
            "Model / Approach": "Proposed Method (Our Project)", 
            "ROUGE-1 (F1)": my_project_scores['ROUGE-1 F1'],
            "ROUGE-2 (F1)": my_project_scores['ROUGE-2 F1'],
            "ROUGE-L (F1)": my_project_scores['ROUGE-L F1']
        }
    ]

    results_df = pd.DataFrame(compare_data)

    print("================ PERFORMANCE COMPARISON TABLE =================")
    print(results_df.to_string(index=False))
    print("===============================================================\n")
    print("Note: Scores are reported as F1-scores scaled to 0-100% range.")
    print("You can copy this table directly into your IEEE paper's 'Results' section.")

if __name__ == "__main__":
    main()
